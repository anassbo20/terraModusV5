function updatelangList(langTosw) { 
    const activLang = document.getElementById(`lang-sw-act`);
    const rempTxt = langTosw.innerHTML;
    langTosw.innerHTML = activLang.innerHTML;
    activLang.innerHTML = rempTxt;
    translateTo(langTosw.getAttribute("data-trans"));
    const rempDataTrans = langTosw.getAttribute("data-trans");
    langTosw.setAttribute("data-trans",activLang.getAttribute("data-trans"));
    activLang.setAttribute("data-trans",rempDataTrans);
}


