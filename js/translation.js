let activatedLang = sessionStorage.getItem("activated-lang");


function reloadTrans() {
  if (activatedLang != null) {
    translateTo(activatedLang)
    const langTosw = document.querySelector(`[data-trans="${activatedLang}"]`);
    console.log(langTosw)
    updatelangList(langTosw);
  }
}

